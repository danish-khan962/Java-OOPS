public class j_02_arithmetic {
    public static void main(String[] args) {
        int var_1 = 100;
        int var_2 = 200;

        //performing arithmetic
        System.out.println("Addition :-> " + (var_1 + var_2));
        System.out.println("Subtraction :-> " + (var_1 - var_2));
        System.out.println("Multiplication :-> " + (var_1 * var_2));
        System.out.println("Divison :-> " + (var_1 / var_2));
    }
}
